package main

import "fmt"

func main()  {
	var a int = 12
	var b int = 10
	fmt.Println("The Value of A is = ", a)
	fmt.Println("The Value of B is = ", b)
	fmt.Println("The Value of A + B is = ", a+b)
	fmt.Println("The Value of A - B is = ", a-b)
	fmt.Println("The Value of A * B is = ", a*b)
	fmt.Println("The Value of A / B is = ", a/b)
	fmt.Println("The Value of A % B is = ", a%b)
}